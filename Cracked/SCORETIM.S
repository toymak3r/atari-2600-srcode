

score_timer:		
.TIMLIN:LDA	#$F0		; USE TEMP0 TO TEST SHOWTIM3 & SHOWTIM0
	STA	TEMP0		; IF SHOWTIM5, 4, 2, OR 1 = 0
	LDA 	R_SHOWTIM5	; THEN NO MORE SHIFTING IS NECESSARY.
	BEQ	.ST4		
	LSR			; IF SHOWTIM3 OR 0 = $F0 NO SHIFTING
	STA 	W_SHOWTIM5
	JMP	.OUT
.ST4:	LDA	R_SHOWTIM4
	BEQ	.ST3
	ASL	
	STA	W_SHOWTIM4
	JMP	.OUT

.ST3:	LDA	R_SHOWTIM3
	BIT	TEMP0		; ONLY NEED SHIFT LEFT NYBBLE
	BEQ	.ST2
	LSR	
	STA	W_SHOWTIM3
	JMP	.OUT

.ST2:	LDA	R_SHOWTIM2
	BEQ	.ST1
	LSR	
	STA	W_SHOWTIM2
	JMP	.OUT

.ST1:	LDA	R_SHOWTIM1
	BEQ	.ST0
	ASL	
	STA	W_SHOWTIM1
	JMP	.OUT
.ST0:	LDA	R_SHOWTIM0
	BIT	TEMP0		; ONLY NEED SHIFT LEFT NYBBLE
	BEQ	.TIMSUP
	LSR	
	STA	W_SHOWTIM0
	BIT	TEMP0
	BNE	.OUT
.TIMSUP:
	lda	#0
	sta	TIMER
.OUT:	rts
